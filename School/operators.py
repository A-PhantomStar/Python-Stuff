print("Tipos de operadores en Python: \nOperadores aritmeticos:\n")
print("Operador suma (3+4) =", 3+4)
print("Operador resta (-5-6)=", -5-6)
print("Operador multiplicacion (5*2)=", 5*2)
print("Operador division (5/2)=", 5/2)
print("Operador modulo (5%2)=", 5%2)
print("Operador cociente (5//2)=", 5//2)
print("Operador exponente (5*2)=", 5*2)

print("Operadores de asignacion (=):")
x=5;
print("El valor de x = 5 es de: ",x)
print("\nOperador de suma a la derecha (+=): ")
x=5; x+=2;
print("El valor de x = 5; x+=2 es de: ",x)
print("\nOperador de resta a la derecha (-=): ")
x=5; x-=2;
print("El valor de x = 5; x-=2 es de: ",x)
print("\nOperador de multiplicacion a la derecha (*=): ")
x=5; x*=2;
print("El valor de x = 5; x*=2 es de: ",x)
print("Operador de devuelve el resto a la derecha (//=): ")
x=5; x//=2;
print("El valor de x = 5; x//=2 es de: ",x)
print("Operador de exponente de valor a la derecha (**=): ")
x=5; x**=2;
print("El valor de x = 5; x**=2 es de: ",x)

print("\nOperadores Relacionales: ")
print("Operador de comparacion igual (==): ")
print("5==2: ", 5==2)
print("Operador de comparacion distinto (!=): ")
print("5!=2: ", 5!=2)
print("Operador de comparacion si es mayor que (>): ")
print("5>2: ", 5>2)
print("Operador de comparacion menor que (<): ")
print("5<2: ", 5<2)
print("Operador de comparacion mayor o igual que (>=): ")
print("5>=2: ", 5>=2)
print("Operador de comparacion menor o igual que (<=): ")
print("5<=2: ", 5<=2)

